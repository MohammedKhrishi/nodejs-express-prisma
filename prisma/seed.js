async function main() {
  console.log(`Start seeding ...`)
  for (const value of userData) {
    const user = await prisma.user.create({
      data: value,
    })
    console.log(`Created user with id: ${user.id}`)
  }
  console.log(`Seeding finished.`)
}

main()
  .then(async () => {
    await prisma.$disconnect()
  })
  .catch(async (e) => {
    console.error(e)
    await prisma.$disconnect()
    process.exit(1)
  })
